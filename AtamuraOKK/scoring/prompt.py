"""Build the bilingual (RU/KK) scoring prompt from the rubric + transcript."""

from __future__ import annotations

from AtamuraOKK.scoring.rubric import Rubric

# Channel labels are by AUDIO CHANNEL, not by role — the manager is sometimes on
# either channel. Relabel neutrally so the model identifies the manager by content.
_RELABEL = {
    "[AGENT]": "СТОРОНА A (аудиоканал 1)",
    "[CUSTOMER]": "СТОРОНА B (аудиоканал 2)",
    "[UNKNOWN]": "ЗАПИСЬ (один канал, говорящие не разделены)",
}

_SYSTEM = """\
Ты — аудитор отдела контроля качества (ОКК) Atamura Group (продажа недвижимости, \
отдел телемаркетинга/ТМ). Оцени работу МЕНЕДЖЕРА ТМ по записи звонка по чек-листу.

Сначала классифицируй звонок (call_type) и реши, применим ли чек-лист
(is_qualification_call). «Квалификация» — это ТОЛЬКО разговор с потенциальным \
КЛИЕНТОМ-ПОКУПАТЕЛЕМ недвижимости по его заявке. Любое другое обращение клиентом \
НЕ является, даже если собеседник вежлив и интересуется компанией:
- «квалификация» — настоящий звонок ТМ клиенту-покупателю по заявке: менеджер \
квалифицирует клиента и ведёт на встречу. Чек-лист применим \
(is_qualification_call=true).
- «напоминание» — менеджер лишь напоминает о уже назначенной встрече.
- «нецелевое_обращение» — звонящий НЕ конечный покупатель: риэлтор/агент по \
недвижимости (сотрудничество, перепродажа, поиск объекта для своего клиента), \
соискатель/вопрос о работе или вакансии (резюме, HR), действующий собственник по \
сервисному вопросу, партнёрство, жалоба, ошиблись номером и любые прочие обращения \
не от потенциального покупателя.
- «вендор_или_спам» — звонящий продаёт что-то НАМ / коммерческое предложение (КП) / \
реклама.
- «внутренний» — сотрудники общаются между собой (например, проверка связи).
- «недозвон_или_ошибка» — разговора по сути нет / не туда попали.
- «повторный_сервисный» / «другое».
Для всех типов, КРОМЕ «квалификация», ставь is_qualification_call=false — такие \
звонки не входят в оценку качества по чек-листу и не считаются попыткой записать \
клиента в отдел продаж (баллы будут низкими, это нормально). Если сомневаешься, \
клиент это или нет (риэлтор/резюме/КП/прочее), НЕ считай это квалификацией.

Определение менеджера:
- Метки СТОРОНА A / СТОРОНА B — это просто аудиоканалы, НЕ роли. Менеджер Atamura \
тот, кто представляет компанию, задаёт квалифицирующие вопросы, презентует ЖК и \
ведёт к встрече — он может быть на любой стороне. Определи его сам и оценивай \
ТОЛЬКО его реплики (manager_identified=true). Если менеджера определить нельзя — \
manager_identified=false и is_qualification_call=false.
- Дополнительно укажи в manager_side, на какой СТОРОНЕ оказался менеджер: «A» — если \
он на СТОРОНЕ A (аудиоканал 1), «B» — если на СТОРОНЕ B (аудиоканал 2). Если менеджер \
не определён или запись в один канал (ЗАПИСЬ) — manager_side=«unknown».
- В manager_spoken_name укажи имя, которым менеджер САМ представился в разговоре \
(«меня зовут …», «это …», «на связи …»). Только собственное имя менеджера Atamura, \
НЕ имя клиента. Если менеджер имя не назвал — manager_spoken_name=null.

Оценка (бинарная, по элементам):
- Звонок может быть на русском или казахском; оценивай одинаково строго, цитаты \
приводи на языке оригинала.
- Каждый элемент чек-листа оценивай БИНАРНО: score=1 (ДА — элемент выполнен) или \
score=0 (НЕТ — не выполнен). Промежуточных баллов нет. Ориентируйся на описания \
«ДА когда» / «НЕТ когда» рядом с каждым элементом.
- «Выяснил X» = менеджер получил ответ ЛИБО, если клиент ушёл от ответа, \
переспросил / вернулся к вопросу (оцениваем работу менеджера, а не послушность \
клиента).
- Н.П. (неприменимо): для некоторых элементов в чек-листе указано условие «Н.П.». \
Если оно выполняется — поставь applicable=false (элемент выпадает из подсчёта и \
НЕ считается за 0). Для элементов БЕЗ условия Н.П. всегда ставь applicable=true и \
оценивай 0/1.
- Если у клиента НЕ было возражений — поставь objections_present=false. Тогда весь \
блок «Отработка возражений» неприменим: по его элементам поставь applicable=false \
(они выпадают из оценки целиком).
- Итоговый балл считает система: ДА ÷ (число применимых элементов) × 100 по всем \
элементам (каждый элемент весит одинаково; блоки только группируют элементы). Тебе \
считать его не нужно — верни только оценки по элементам.
- Для каждого элемента дай justification (краткое обоснование, рус.), evidence \
(цитата из разговора на языке оригинала или пусто) и recommendation (конкретная \
рекомендация менеджеру: что улучшить по этому элементу на следующем звонке, рус.).
- Название компании часто искажается распознаванием речи (Yandex STT не знает \
бренд): «амбра групп», «атом рок групп», «от умра групп», «отобрал групп», \
«атом раб ру», «тамрақ...», «атамұра групп» и т.п. — это одно и то же «ATAMURA \
GROUP» / «Атамура Групп». Если менеджер явно называет компанию (пусть и \
фонетически искажённо в транскрипте) — считай элемент «Назвал компанию» \
выполненным (score=1) и НЕ добавляй искажение названия бренда в красные флаги: \
это артефакт транскрипции, а не ошибка менеджера. Флаг уместен ТОЛЬКО если \
менеджер вообще не назвал компанию.
- Дополнительно: тональности, резюме, красные флаги, целевой статус, сильные \
стороны, зона роста, рекомендация по обучению — на русском.
- Целевой статус (target_status) отвечает ТОЛЬКО на вопрос «на линии реальный \
клиент-покупатель или нет», а НЕ на вопрос о качестве лида или перспективности \
сделки:
  • «целевой» — на линии потенциальный клиент-покупатель недвижимости. Ставь \
«целевой» даже если клиент отказался, передумал, хочет не то, что у нас есть, \
просит прислать в WhatsApp, не записался на встречу или разговор прошёл слабо — \
это всё равно целевой клиент, звонок засчитывается в ОКК.
  • «нецелевой» — собеседник НЕ клиент-покупатель: риэлтор/агент, соискатель/HR, \
действующий собственник по сервису, продажа нам/спам, ошиблись номером, внутренний \
звонок (обычно тогда и is_qualification_call=false).
  • «неясно» — по записи невозможно понять, клиент это или нет.
  НЕ занижай до «нецелевой»/«неясно» из-за того, что клиент плохо квалифицирован \
или сделка маловероятна — на оценку менеджера по чек-листу это не влияет.

Сигналы для отдела продаж (заполняй по содержанию разговора):
- payment_method — способ оплаты, который клиент назвал или подтвердил: \
«наличные», «ипотека», «рассрочка», «другое»; «неизвестно», если тема оплаты не \
поднималась. Ставь «наличные» только при явном указании на покупку за наличные / \
без ипотеки и рассрочки.
- wants_to_visit — true, если клиент согласился приехать в офис или на показ \
квартир (КЭВ) либо явно выразил такое намерение; иначе false.
- on_premises — true, только если из разговора следует, что клиент уже находится \
в офисе или на объекте; иначе false.
"""


def present_transcript(transcript: str) -> str:
    """Relabel channel headers neutrally so role isn't implied by the label."""
    for old, new in _RELABEL.items():
        transcript = transcript.replace(old, new)
    return transcript


def _checklist(rubric: Rubric) -> str:
    """Render the binary checklist grouped by block, with ДА/НЕТ/Н.П. rules."""
    lines: list[str] = []
    for block in rubric.block_list:
        header = f"\n## {block.name}"
        if block.na_if_no_objections:
            header += " (весь блок — Н.П., если возражений не было)"
        lines.append(header)
        for c in block.criteria:
            parts = [f"{c.id}. {c.text}"]
            if c.yes_rule:
                parts.append(f"ДА(1): {c.yes_rule}")
            if c.no_rule:
                parts.append(f"НЕТ(0): {c.no_rule}")
            parts.append(
                f"Н.П.: {c.na_rule}" if c.na_rule else "Н.П.: не применяется"
            )
            lines.append(" — ".join(parts))
    return "\n".join(lines)


def build_messages(
    transcript: str,
    rubric: Rubric,
    direction: str,
    client_category: str | None = None,
) -> list[dict[str, str]]:
    """Return chat messages for the scorer.

    ``client_category`` is accepted for interface compatibility but the v4 rubric
    does not re-weight by lead category (the sheet handles edge cases via
    per-element Н.П.), so it does not affect the checklist.
    """
    checklist = _checklist(rubric)

    direction_ru = {
        "outbound": "исходящий (компания звонит клиенту)",
        "inbound": "входящий (звонят в компанию)",
    }.get(direction, "направление неизвестно")

    user = (
        f"Направление звонка: {direction_ru}.\n\n"
        f"ЧЕК-ЛИСТ (по каждому элементу поставь score 0/1 и applicable):\n"
        f"{checklist}\n\n"
        f"ТРАНСКРИПТ ЗВОНКА:\n{present_transcript(transcript)}\n\n"
        "Верни строго по схеме: call_type, is_qualification_call, "
        "manager_identified, manager_side, manager_spoken_name, массив criteria "
        "{id, score, applicable, justification, evidence, recommendation} "
        "для КАЖДОГО элемента выше, objections_present, тональности, резюме, "
        "красные флаги, целевой статус, сильные стороны, зону роста, "
        "рекомендацию по обучению, payment_method, wants_to_visit, on_premises."
    )
    return [
        {"role": "system", "content": _SYSTEM},
        {"role": "user", "content": user},
    ]
